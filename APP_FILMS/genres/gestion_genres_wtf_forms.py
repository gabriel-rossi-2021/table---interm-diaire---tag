from flask_wtf import FlaskForm
from wtforms import StringField
from wtforms import SubmitField
from wtforms.validators import Length
from wtforms.validators import Regexp


class FormWTFAjouterCollaborateur(FlaskForm):
    """
        Dans le formulaire "genres_ajouter_wtf.html" on impose que le champ soit rempli.
        Définition d'un "bouton" submit avec un libellé personnalisé.
    """
    nom_famille_regexp = "^([A-Z]|[a-zÀ-ÖØ-öø-ÿ])[A-Za-zÀ-ÖØ-öø-ÿ]*['\- ]?[A-Za-zÀ-ÖØ-öø-ÿ]+$"
    nom_famille_wtf = StringField("Clavioter le collaborateur ", validators=[Length(min=2, max=20, message="min 2 max 20"),
                                                                   Regexp(nom_famille_regexp,
                                                                          message="Pas de chiffres, de caractères "
                                                                                  "spéciaux, "
                                                                                  "d'espace à double, de double "
                                                                                  "apostrophe, de double trait union")
                                                                   ])
    prenom_regexp = "^([A-Z]|[a-zÀ-ÖØ-öø-ÿ])[A-Za-zÀ-ÖØ-öø-ÿ]*['\- ]?[A-Za-zÀ-ÖØ-öø-ÿ]+$"
    prenom_wtf = StringField("Taper le prénom du collaborateur ",
                             validators=[Length(min=2, max=20, message="min 2 max 20"),
                                         Regexp(prenom_regexp,
                                                message="Pas de chiffres, de caractères "
                                                        "spéciaux, "
                                                        "d'espace à double, de double "
                                                        "apostrophe, de double trait union")
                                         ])
    submit = SubmitField("Enregistrer genre")


